package basics;

public class IfCondition {

	public static void main(String[] args) {

		// Equal ==
		// Not Equal !=
		// Greater than Equal >=
		// Greater Than >
		// Less Than <

		int num1 = 130;
		int num2 = 122;

		if (num1 > num2) {
			System.out.println("Num1 is bigger");
		}

		// If num1 = 110 and num2=111; It does not print any result.
	}
}
